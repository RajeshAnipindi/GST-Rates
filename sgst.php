<p class="h4 text-center">State Goods & Service Tax (SGST)</p>
<p class="text-justify">GST is an important part of GST. It stands for State Goods & Services Tax as per the 2016 GST bill. Various taxations and levies under the state authority are subsumed by SGST as one uniform taxation. It includes the amalgamation of State Sales Tax, Luxury Tax, Entertainment Tax, Levies on Lottery, Entry Tax, Octroi and other taxations related to the movement of commodities and services under state authority through one uniform taxation- SGST.
Revenue collected under SGST belongs to the State Government. However, the mainstream framework of the state governing body will be supervised by the central government. Each state will be having their own State Authority to collect SGST. Under GST, SGST is a tax levied on Intra State supplies of both goods and services by the State Government and will be governed by the SGST Act. As explained above, CGST will also be levied on the same Intra State supply but will be governed by the Central Government. </p>
<p class="h4 text-primary">Meaning of SGST or State GST</p>
<p class="text-justify">State GST or SGST is a tax levied under the SGST Act on intra-state supplies of goods and services, that is administered by the respective State Government. SGST lability can be set off against SGST or IGST input tax credit only.</p>
<p class="h4 text-primary">Highlights of SGST</p>
<ul>
  <li>SGST is levied by the State Governments through a statute on all transactions of supply of goods and services.</li>
  <li>SGST would be paid to the accounts of the respective State Government.</li>
</ul>

