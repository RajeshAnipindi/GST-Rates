<p class="h4 text-center" style="padding-top: 10%;">Central Goods & Service Tax (CGST)</p>
<p class="text-justify">As per the Central Goods & Services Tax Act 2016, CGST is the centralized part of GST that subsumes the present central taxations and levies- Central Sales Tax, Central Excise Duty, Services Tax, Excise Duty under Medical & Toiletries Preparation Act, Additional Excise Duties Countervailing Duty (CVD), Additional Custom Duty and other centralized taxations.
CGST is applicable on the supply of goods and services of standard services and commodities which can be amended periodically by a specialized body under the central government. The revenue collected under CGST belongs to the central government. The input tax is given to the state governments which they can utilize only against the payment of CGST. Under GST, CGST is a tax levied on Intra State supplies of both goods and services by the Central Government and will be governed by the CGST Act. SGST will also be levied on the same Intra State supply but will be governed by the State Government.
This implies that both the Central and the State governments will agree on combining their levies with an appropriate proportion for revenue sharing between them. However, it is clearly mentioned in Section 8 of the GST Act that the taxes be levied on all Intra-State supplies of goods and/or services but the rate of tax shall not be exceeding 14%, each. Central GST or CGST would be levied under the CGST Act on the intra-state supplies of goods and services. Hence in case of intra-state supplies of goods and services, both the Central and State government would combine their levies with an appropriate revenue sharing agreement between them. The power to levy CGST and SGST has been provided for in Section 8 of the GST Act, where it has been mentioned that:
The following taxes shall be levied on all intra-state supplies of goods, or services or both, at such rates specified in the Schedule to the said Act on the recommendation of the Council, but not exceeding 14%, each. Such CGST and SGST is to be paid by a taxable person.</p>
<p class="h4 text-primary">HIGHLIGHTS</p>
