<p class="h4 text-center" style="padding-top: 20%;">Integrated Goods & Service Tax (IGST)</p>
<p class="text-justify">GST focuses on the concept of one tax, one nation. IGST stands for Integrated Goods and Services Tax which is charged on the supply of commodities and services from one state to another state. For example, if the supply of goods and services occurs between Gujarat and Maharashtra, IGST will be applicable. </p>
<p class="h4 text-primary">Meaning of IGST or Integrated GST</p>
<p class="text-justify">Integrated GST or IGST is the tax levied under the IGST Act on the supply of any goods and/or services in the course of inter-state trade across India. Further, IGST would include any supply of goods and/or services in the course of import into India and export of goods and/or services from India. IGST will replace the present Central State Tax which is levied on the inter-state sales of goods. Thus, IGST would be applicable for all inter-state transactions, import and export of goods and/or services.</p>
<p class="h4 text-primary">Highlights of IGST</p>
<ul>
  <li>Central Government would levy and collect IGST instead of CGST or SGST.</li>
  <li>Levied on inter-state supply of goods and/or services.</li>
  <li>Includes import of goods and/or services.</li>
  <li>Exports would be zero rated.</li>
  <li>IGST would be shared between the Central and State Government.</li>
</ul>
<p class="text-justify">Under Article 269A of the Indian Constitution, the inter-state trade and commerce activities that involve the movement of commodities and services shall be levied with an integrated tax (IGST) under the GST regime. The Government of India will collect the revenue under IGST. Further changes can be made by the Goods and Services Tax Council of India. Under GST, IGST is a tax levied on all Inter-State supplies of goods and/or services and will be governed by the IGST Act. IGST will be applicable on any supply of goods and/or services in both cases of import into India and export from India.</p>

<p class="h4 text-danger">NOTE : </p>
<ul>
  <li>Exports would be zero-rated.</li>
  <li>Tax will be shared between the Central and State Government.</li>
</ul>


